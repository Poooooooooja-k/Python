# for loops can iterate over a sequence of iterable objects in python
name="abhishek"
for i in name: 
    print(i)
colors=["red","green","blue","yellow"]
for color in colors:
    print(color)
    for i in color:
        print(i)
for k in range(5):
    print(k)
for k in range(1,5):
    print(k)
for i in range(1,12,3):
    print(i)
